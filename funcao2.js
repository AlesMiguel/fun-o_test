function somar(a, b){
    let somar = a + b
    // console.log(resultado_da_soma)
    console.log (`Valores para Somar ${a} + ${b} = ${somar}`)
    
    // somar (10, 2)
}

function sub(a, b){
    let subtrair = a - b
    // console.log (sub)
    console.log(`Valores para Subtrair ${a} - ${b} = ${subtrair}`)
    
    // sub (10, 8)
}


